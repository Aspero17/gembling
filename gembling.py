users_file = "/users_data.csv"


# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –∑–∞–≥—Ä—É–∑–∫–∏ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –∏–∑ CSV-—Ñ–∞–π–ª–∞
def load_users():
    users = []
    try:
        with open(users_file, "r", encoding="utf-8") as file:
            reader = csv.DictReader(file)
            for row in reader:
                users.append({"user_id": row["user_id"], "username": row["username"]})
    except FileNotFoundError:
        pass
    return users


# –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –≤ CSV-—Ñ–∞–π–ª
def save_users(users):
    with open(users_file, "w", encoding="utf-8", newline="") as file:
        fieldnames = ["user_id", "username"]
        writer = csv.DictWriter(file, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(users)


